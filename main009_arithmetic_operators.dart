/// Arithmetic Operators
/// run: dart main009_arithmetic_operators.dart

main() {

  var x = 7;
  var y = 2.5;

  print(x + y); /// Addition
  print(x - y); /// Subtraction
  print(x * y); /// Multiplication
  print(x / y); /// Division
  print(x ~/ y);  /// Truncating Division
  print(x % y); /// Euclidean Modulo
  print(-x);  /// Negative

}