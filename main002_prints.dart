/// main: the default function of execution when a .dart file is executed
/// Many ways to print some text
/// run: dart main002_prints.dart

main() {

  print("André Barcelos Silva");    // String
  print("Hello \'World\'!");        // String
  print('Hello \"World\"!');        // String
  print("Hello \"World\"!");        // String
  print('Hello \'World\'!');        // String
  print("Hello " + "World!");       // Concatened String
  print(true);                      // Boolean
  print(false);                     // Boolean
  print(1);                         // Integer
  print(1.99);                      // Double
  print([1, 'A', 2.95, "Complex"]); // List
  print({"Key": "Value", "Name": "John", "LastName": "Doe"}); // Map
  
}